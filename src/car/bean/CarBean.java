package car.bean;

public class CarBean {

	
	private String cCode;
	private String cType;
	private String cSize;
	private String cName;
	private int cPrice;
	private String cGear;
	private String cFuel;
	private String cSeat;
	private String cFile;
    
	
	public String getcCode() {
		return cCode;
	}
	public void setcCode(String cCode) {
		this.cCode = cCode;
	}
	public String getcType() {
		return cType;
	}
	public void setcType(String cType) {
		this.cType = cType;
	}
	public String getcSize() {
		return cSize;
	}
	public void setcSize(String cSize) {
		this.cSize = cSize;
	}
	public String getcName() {
		return cName;
	}
	public void setcName(String cName) {
		this.cName = cName;
	}
	public int getcPrice() {
		return cPrice;
	}
	public void setcPrice(int cPrice) {
		this.cPrice = cPrice;
	}
	public String getcGear() {
		return cGear;
	}
	public void setcGear(String cGear) {
		this.cGear = cGear;
	}
	public String getcFuel() {
		return cFuel;
	}
	public void setcFuel(String cFuel) {
		this.cFuel = cFuel;
	}
	public String getcSeat() {
		return cSeat;
	}
	public void setcSeat(String cSeat) {
		this.cSeat = cSeat;
	}
	public String getcFile() {
		return cFile;
	}
	public void setcFile(String cFile) {
		this.cFile = cFile;
	}
	

}
